/** Builds the standalone userscript using esbuild.
 * This will:
 * 1. Update the package version across the entire project
 * 2. Bundle the JS and CSS sources (esbuild)
 * 3. Compress and obfuscate the bundled JS file (terser)
 * 4. Run the CSS selector mangler (cssMangler.js)
 * 5. Embed the CSS and font into dist/Chromora.user.js
 * @since 0.0.6
*/

// ES Module imports
import esbuild from 'esbuild';
import crypto from 'crypto';
import fs from 'fs';
import { execSync } from 'child_process';
import { consoleStyle } from './utils.js';
import mangleSelectors from './cssMangler.js';

import { createRequire } from 'module';
const require = createRequire(import.meta.url);
// CommonJS imports (require)
const terser = require('terser');

const isGitHub = !!process.env?.GITHUB_ACTIONS; // Is this running in a GitHub Action Workflow?'
const outputName = 'Chromora.user.js';
const outputPath = `dist/${outputName}`;
const bundledCSSPath = 'dist/Chromora.user.css';
const cssMapPath = 'dist/Chromora.user.css.map.json';
const updateURL = `https://raw.githubusercontent.com/alexeygasenko/Chromora/main/${outputPath}`;

// Remove build flavors that are no longer distributed.
for (const legacyPath of [
  'dist/BlueMarble.user.js',
  'dist/BlueMarble.user.css',
  'dist/BlueMarble.user.css.map.json',
  'dist/BlueMarble-Standalone.user.js',
  'dist/BlueMarble-For-GreasyFork.user.js',
  'dist/BlueMarble-For-GreasyFork.user.css'
]) {
  fs.rmSync(legacyPath, { force: true });
}

/** Appends a build hash comment to an output file.
 * The hash is based on the file contents before the hash comment is added.
 * @param {string} path - Path to the file
 * @param {'js' | 'css'} type - Output type for comment syntax
 * @returns {string} The short build hash
 * @since 0.92.0
 */
function appendBuildHashComment(path, type = 'js') {
  const content = fs.readFileSync(path, 'utf8').trimEnd();
  const hash = crypto.createHash('sha256').update(content, 'utf8').digest('hex').slice(0, 12);
  const comment = (type == 'css')
    ? `/* Build Hash: ${hash} */`
    : `// Build Hash: ${hash}`;

  fs.writeFileSync(path, `${content}\n\n${comment}\n`, 'utf8');
  return hash;
}

